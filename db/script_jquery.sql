-- Tabla de facultades
CREATE TABLE Facultades(
	codigo_fac varchar(50) NOT NULL PRIMARY KEY,
	nombre_fac varchar(50) NOT NULL UNIQUE
);
-- Tabla de programas
CREATE TABLE Programas(
	codigo_pro varchar(50) NOT NULL PRIMARY KEY,
	nombre_pro varchar(50) NOT NULL UNIQUE,
	facultad varchar(50) NOT NULL REFERENCES Facultades(codigo_fac)
);
-- Tabla de estudiantes
CREATE TABLE Estudiantes(
	codigo_est varchar(50) NOT NULL PRIMARY KEY,
	identi varchar(50) NOT NULL,
	nombre_est varchar(50) NOT NULL,
	direccion varchar(50),
	telefono varchar(50),
	email varchar(50),
	semestre integer,
	contraseña varchar(50) NOT NULL,
	programa varchar(50) NOT NULL REFERENCES Programas(codigo_pro),
	estado bool,
	num_cance integer
);
-- Tabla de docentes
CREATE TABLE Docentes(
	codigo_doc varchar(50) NOT NULL PRIMARY KEY,
	identi varchar(50) NOT NULL,
	nombre_doc varchar(50) NOT NULL,
	direccion varchar(50),
	telefono varchar(50),
	email varchar(50),
	contraseña varchar(50) NOT NULL,
	programa varchar(50) NOT NULL REFERENCES Programas(codigo_pro),
	estado bool
);
-- Tabla de administradores
CREATE TABLE Administradores(
	codigo_adm varchar(50) NOT NULL PRIMARY KEY,
	identi varchar(50) NOT NULL,
	nombre_adm varchar(50) NOT NULL,
	direccion varchar(50),
	telefono varchar(50),
	email varchar(50),
	contraseña varchar(50) NOT NULL,
	facultad varchar(50) NOT NULL REFERENCES Facultades(codigo_fac),
	estado bool
);
-- Tabla de las materias
CREATE TABLE Materias(
	codigo_mat varchar(50) NOT NULL PRIMARY KEY,
	nombre_mat varchar(60) NOT NULL,
	creditos integer,
	semestre integer,
	programa varchar(50) NOT NULL REFERENCES Programas(codigo_pro)
);
-- Table de los grupos
CREATE TABLE Grupos(
	codigo_gru varchar(50) NOT NULL PRIMARY KEY,
	nombre_gru varchar(50) NOT NULL,
	materia varchar(50) NOT NULL REFERENCES Materias(codigo_mat),
	docente varchar(50) NOT NULL REFERENCES Docentes(codigo_doc),
	cupo integer
);
-- Tabla de las matriculas
CREATE TABLE Matriculas(
	codigo_matri integer NOT NULL PRIMARY KEY,
	estudiante varchar(50) NOT NULL REFERENCES Estudiantes(codigo_est),
	grupo varchar(50) NOT NULL REFERENCES Grupos(codigo_gru),
	materia varchar(50) NOT NULL REFERENCES Materias(codigo_mat),
	estado varchar(50)
);
